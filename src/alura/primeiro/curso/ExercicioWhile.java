package alura.primeiro.curso;

public class ExercicioWhile {
    public static void main(String[] args) {
        int contador = 0;
        int total = 0;
        while (contador <= 10) {
            total = total + contador;
            System.out.println(total);
            contador++;
        }
        System.out.println("-----------");
        System.out.println("Contador: Resultado Final " + contador);
        System.out.println("Contador: Resultado Final " + contador);
    }
}
